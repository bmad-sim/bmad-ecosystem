!---------------------------------------------------------------------------------------------
!+
! Subroutine photon_reflection (graze_angle_in, energy, surface, graze_angle_out, phi_out)
!
! Routine to reflect a photon from a surface including both diffuse and specular reflections.
!
! Input:
!   graze_angle_in  -- Real(rp): Incident grazing (not polar) angle in radians.
!   energy          -- Real(rp): Photon energy in eV.
!   surface         -- photon_reflect_surface_struct: surface info
!
! Output:
!   graze_angle_out -- Real(rp): graze_angle in radians. 
!   phi_out         -- Real(rp): Azimuthal angle in radians.
!-

subroutine photon_reflection (graze_angle_in, energy, surface, graze_angle_out, phi_out)

implicit none

type (photon_reflect_surface_struct), target :: surface

real(rp) graze_angle_in, energy, graze_angle_out,  phi_out
real(rp) p_spec, r, lambda

! If the photon energy is lower than 1eV then use 1eV in the calculation.
! Decide if reflection is specular.

lambda = h_planck * c_light / max(1.0_rp, energy)
P_spec = exp(-(fourpi * surface%surface_roughness_rms * sin(graze_angle_in) / lambda)**2)
call ran_uniform(r)

if (r < P_spec) then   ! Is specular
  graze_angle_out = graze_angle_in
  phi_out = 0

else
  call photon_diffuse_scattering (graze_angle_in, energy, surface, graze_angle_out, phi_out)
endif

end subroutine photon_reflection

!---------------------------------------------------------------------------------------------
!---------------------------------------------------------------------------------------------
!---------------------------------------------------------------------------------------------
!+
! Subroutine photon_diffuse_scattering (graze_angle_in, energy, surface, graze_angle_out, phi_out, diffuse_param)
!
! Routine to simulate the diffuse scattering of photons. The outgoing angles are
! choosen using the Dugan distribution.
!
! Also see: photon_reflection.
! Use photon_reflection_std_surface_init or read_surface_reflection_file to get surface info.
!
! Input:
!   graze_angle_in  -- Real(rp): Incident grazing (not polar) angle in radians.
!   energy          -- Real(rp): Photon energy in eV.
!   surface         -- photon_reflect_surface_struct: surface info
!
! Output:
!   graze_angle_out -- Real(rp): graze_angle in radians. 
!   phi_out         -- Real(rp): Azimuthal angle in radians.
!   diffuse_param   -- diffuse_param_struct, optional: Internal parameters used in the calculation.
!                        This is used for diagnostics and is not used in standard simulations.
!-

subroutine photon_diffuse_scattering (graze_angle_in, energy, surface, graze_angle_out, phi_out, diffuse_param)

use random_mod
!! use nr, only: chebft, chint, chebev   !! NR has been removed from Bmad Distributions.
use super_recipes_mod, only: super_rtsafe

implicit none

type (photon_reflect_surface_struct), target :: surface
type (diffuse_param_struct) d_param
type (cheb_diffuse_struct) cheb_param
type (diffuse_param_struct), optional :: diffuse_param

real(rp) graze_angle_in, energy, graze_angle_out,  phi_out
real(rp) sigma, t, ctheta2, sign_phi, tot_integral, rel_integral_err, integral, old_integral
real(rp) fl, fh, df, r, p_spec, ran1, ran2, integral_err(0:50)

integer i, ix, j, n_pt, status

character(*), parameter :: r_name = 'photon_diffuse_scattering'

logical ok

! If the photon energy is lower than 1eV then use 1eV in the calculation.

sigma = surface%surface_roughness_rms
T = surface%roughness_correlation_len
d_param%y = sin(graze_angle_in)
d_param%lambda = h_planck * c_light / max(1.0_rp, energy)

! Pick random numbers

call ran_uniform(ran1)

call ran_uniform(ran2)
if (ran2 > 0.5) then
  sign_phi = 1
  ran2 = 2 * ran2 - 1
else
  sign_phi = -1
  ran2 = 2 * ran2 
endif

! Fit the probability distribution in x = sin(graze_angle_out)
! Also compute the coefficients fo the cumulative distribution in x

if (diffuse_com%use_spline_fit) then

  ! First construct the spline fit for the probability function

  n_pt = -1

  call eval_prob_x (d_param%prob_spline(0), 0.0_rp)
  call eval_prob_x (d_param%prob_spline(1), d_param%y)
  call eval_prob_x (d_param%prob_spline(2), 1.0_rp)

  call insert_spline_point (1)
  call insert_spline_point (0)

  do i = 1, 20
    call spline_akima(d_param%prob_spline(0:n_pt), ok)
    tot_integral = 0
    do j = 0, n_pt-1
      ! Use abs so not to be confused when the integral is negative due to fitting errors.
      ! This should not affect the results significantly.
      tot_integral = tot_integral + abs(spline1(d_param%prob_spline(j), d_param%prob_spline(j+1)%x0, -1))  
    enddo

    do j = 1, n_pt-1
      call integral_err_calc(j)
    enddo

    rel_integral_err = sum(integral_err(1:n_pt-1)) / tot_integral

    ix = maxloc(integral_err(1:n_pt-1), 1)

    if (i < 4) cycle
    if (rel_integral_err < diffuse_com%area_err_tol) exit
    if (i == 20) exit
    call insert_spline_point (ix)
    call insert_spline_point (ix-1)
  enddo

  d_param%chx_norm = tot_integral

  ! Integrate the probability function and find where probability integral from 0 to cos(theta2) equals ran1.
  ! First integrate over the spline intervals to bracket cos(theta2) and then use super_rtsafe to interpolate.

  integral = 0
  do j = 0, n_pt-1
    old_integral = integral
    ! Use abs so not to be confused when the integral is negative due to fitting errors.
    integral = integral + abs(spline1(d_param%prob_spline(j), d_param%prob_spline(j+1)%x0, -1))
    if (integral >= ran1 * tot_integral) exit
  enddo

  ctheta2 = super_rtsafe (d_integral, d_param%prob_spline(j)%x0, d_param%prob_spline(j+1)%x0, &
                                      1.0e-12_rp, 1.0e-5_rp, status)

! Fit the probability distribution to Chebyshev polynomials.
! This is known to produce bad results for smoother surfaces so eventually this
! will be eliminated. Keep for now for cross-check purposes.
! NOTE: Due to the removal of NR, the calls to NR routines have been commented out 2021/12. Look for "!!NR"
else
!!NR  cheb_param%cch = chebft(0.0_rp, 1.0_rp, n_cheb_term$, prob_x_diffuse_vec)
!!NR  cheb_param%cch_int = chint (0.0_rp, 1.0_rp, cheb_param%cch)
!!NR  !  evaluate the normalization constant
!!NR  d_param%chx_norm = chebev(0.0D0, 1.0D0, cheb_param%cch_int, 1.0D0)
!!NR  ! find the value of x for which the cumulative probability equals the random number
!!NR  ctheta2 = super_rtsafe(cumulx, 0.0E0_rp, 1.0E0_rp, 1.0e-12_rp, 1.0D-5, status)
endif

! Evaluate the normalization constant for the cumulative probability in phi, for this x

graze_angle_out = asin(ctheta2)
d_param%x = ctheta2
d_param%c_norm = cos_phi(sigma, T, pi, d_param)

! find the value of phi for which the cumulative probability equals ran2

call cumulr(0.0_rp, fl, df, status)
call cumulr(pi, fh, df, status)
if ((fl > 0 .and. fh > 0) .or. (fl < 0 .and. fh < 0)) then 
  call out_io (s_fatal$, r_name, 'ROOT NOT BRACKETED FOR PHI CALC!', 'fl, fh: \2es14.5\ ', r_array = [fl, fh])
  call output_specular_reflection_input_params(d_param, surface)
endif

phi_out = sign_phi * super_rtsafe(cumulr, 0.0E0_rp, pi, 1.0e-12_rp, 1.0e-5_rp, status)

if (present(diffuse_param)) then
  diffuse_param = d_param
  diffuse_param%n_pt_spline = n_pt
endif

